/** Get the full design context for an industry */
export const getIndustryDesignContext = (industry: string): {
  palette: IndustryPalette | undefined;
  reasoning: IndustryReasoning | undefined;
  styles: DesignStyle[];
  key: string | undefined;
} => {
  const key = matchIndustryKey(industry) ?? industry;
  const palette = INDUSTRY_PALETTES[key as keyof typeof INDUSTRY_PALETTES];
  const reasoning = INDUSTRY_REASONING[key as keyof typeof INDUSTRY_REASONING];
  const recommendedStyleKeys = reasoning?.recommendedStyles ?? [];
  const styles = recommendedStyleKeys
    .map(s => DESIGN_STYLES[s as keyof typeof DESIGN_STYLES])
    .filter(Boolean);
  return { palette, reasoning, styles, key };
};

/** Format an industry palette for prompt inclusion */
export const formatPaletteForPrompt = (key: string, palette: IndustryPalette): string => {
  return [
    `**${key} Color Palette**`,
    `  Primary: ${palette.primary}`,
    `  Secondary: ${palette.secondary}`,
    `  Accent/CTA: ${palette.accent}`,
    `  Background: ${palette.background}`,
    `  Text: ${palette.text}`,
    `  Border: ${palette.border}`,
    `  Success: ${palette.success} | Warning: ${palette.warning} | Error: ${palette.error} | Info: ${palette.info}`,
    `  Psychology: ${palette.notes}`,
  ].join('\n');
};

/** Format typography pairing for prompt inclusion */
export const formatTypographyForPrompt = (pairing: TypographyPairing): string => {
  return [
    `**${pairing.id}**: ${pairing.headingFont} + ${pairing.bodyFont}`,
    `  Mood: ${pairing.mood}`,
    `  Use cases: ${pairing.useCases.join(', ')}`,
    `  Scale: h1=${pairing.scale.h1}, h2=${pairing.scale.h2}, body=${pairing.scale.body}`,
    `  Google Fonts: ${pairing.googleFontsUrl}`,
  ].join('\n');
};

/** Format design style for prompt inclusion */
export const formatStyleForPrompt = (style: DesignStyle): string => {
  return [
    `**${style.name}**: ${style.description}`,
    `  AI Keywords: ${style.aiPromptKeywords}`,
    `  Do: ${style.dos.slice(0, 3).join('; ')}`,
    `  Don't: ${style.donts.slice(0, 3).join('; ')}`,
    `  Compatibility: dark=${style.compatibility.darkMode}, mobile=${style.compatibility.mobile}, a11y=${style.compatibility.accessibility}, perf=${style.compatibility.performance}`,
  ].join('\n');
};

/** Format industry reasoning for prompt inclusion */
export const formatReasoningForPrompt = (key: string, reasoning: IndustryReasoning): string => {
  return [
    `**${key} Design Intelligence**`,
    `  Styles: ${reasoning.recommendedStyles.join(', ')}`,
    `  Color mood: ${reasoning.colorMood}`,
    `  Typography mood: ${reasoning.typographyMood}`,
    `  Must have: ${reasoning.decisionRules.must_have}`,
    `  Anti-patterns: ${reasoning.antiPatterns.slice(0, 3).join('; ')}`,
    `  Severity: ${reasoning.severity}`,
  ].join('\n');
};

/** Get all design data formatted for AI prompt context */
export const getFullDesignContextForPrompt = (industry: string): string => {
  const ctx = getIndustryDesignContext(industry);
  const parts: string[] = [`# Design Intelligence for "${industry}"\n`];

  if (ctx.palette) {
    parts.push(formatPaletteForPrompt(ctx.key ?? industry, ctx.palette));
  }
  if (ctx.reasoning) {
    parts.push(formatReasoningForPrompt(ctx.key ?? industry, ctx.reasoning));
  }
  if (ctx.styles.length > 0) {
    parts.push('**Recommended Design Styles:**');
    ctx.styles.forEach(s => parts.push(formatStyleForPrompt(s)));
  }

  return parts.join('\n\n');
};